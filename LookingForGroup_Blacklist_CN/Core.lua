	local leader = info.leaderName:lower()
	if not leader:find("-") then
		leader =  leader.."-"..realmname
	end
	if ignores[leader] then
		return 8
	end
	if ignores[strsplit("-",leader)] then
		return 8
	end
	for i=1,#ignores do
		if leader:find(ignores[i]) then
			return 8
		end
	end
end,GetRealmName)

